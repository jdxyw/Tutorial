#pragma mark - Table view delegate

- (void)tableView:(UITableView *)tableView didSelectRowAtIndexPath:(NSIndexPath *)indexPath
{
    NSUInteger row=[indexPath row];
    UITableViewCell *cell=[tableView cellForRowAtIndexPath:indexPath];
    UISwitch *switchview=(UISwitch *)[cell viewWithTag:kSwitch];
    
    NSString *onstring=(switchview.on)? @"is on":@"NOT ON";
    
    UIAlertView *alert=[[UIAlertView alloc] initWithTitle:@"Row selected" message:onstring delegate:nil cancelButtonTitle:@"Thanks!" otherButtonTitles:nil, nil];
    [alert show];
    [alert release];
    [onstring release];
}

